#include "../push_swap.h"

int	is_sorted(t_stack st)
{
	int	i;

	i = 0;
	while (i < st.len - 1)
	{
		if (st.stack[i] < st.stack[i + 1])
			return (0);
		i++;
	}
	return (1);
}
